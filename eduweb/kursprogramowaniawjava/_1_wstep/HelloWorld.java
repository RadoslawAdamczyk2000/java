class HelloWorld { // Stworzenie klasy o nazwie Hello World
    public static void main(String [] args){ // W klasie tworzę metodę "main", która jest punktem wejścia do programu. Jest to standardowa metoda, która musi istnieć w każdym programie Javy
        System.out.println("Hello World"); // W metodzie "main" używam metody "println" z klasy "System" do wyświetlania napisu "Hello World" w konsoli
    }
}

// Nazwa pliku powinna odpowiadać nazwie klasy